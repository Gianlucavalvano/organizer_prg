def aggiungi_progetto(nome, note, stato_id):
    conn = connetti()
    c = conn.cursor()
    owner_filter, owner_params = owner_filter_sql("p")
    uid = _current_user_id()
    owner_filter = " AND owner_user_id = ?" if (uid is not None and not _is_admin()) else ""
    c.execute(
        f"""
        SELECT COALESCE(MAX(ordine_manuale), 0)
        FROM progetti
        WHERE attivo = 1 AND (archiviato = 0 OR archiviato IS NULL){owner_filter}
        """,
        (uid,) if owner_filter else None,
    )
    next_ordine = (c.fetchone()[0] or 0) + 1
    data_ins = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    c.execute(
        """
        INSERT INTO progetti (nome_progetto, note, data_inserimento, id_stato, attivo, ordine_manuale, owner_user_id, ticket_interno, ticket_esterno)
        VALUES (?, ?, ?, ?, 1, ?, ?, '', '')
        """,
        (nome, note, data_ins, stato_id, next_ordine, uid),
    )
    conn.commit()
    conn.close()


def riallinea_ordine_progetti():
    """
    Garantisce un ordine continuo (1..N) per i progetti attivi non archiviati.
    Utile per mantenere stabile lo spostamento su/giu'.
    """
    conn = connetti()
    c = conn.cursor()
    c.execute(
        """
        SELECT id_progetto
        FROM progetti
        WHERE attivo = 1 AND (archiviato = 0 OR archiviato IS NULL)
        ORDER BY COALESCE(ordine_manuale, id_progetto) ASC, id_progetto ASC
        """
    )
    ids = [r[0] for r in c.fetchall()]
    for idx, id_progetto in enumerate(ids, start=1):
        c.execute(
            "UPDATE progetti SET ordine_manuale = ? WHERE id_progetto = ?",
            (idx, id_progetto),
        )
    conn.commit()
    conn.close()


def sposta_progetto(id_progetto, direzione):
    """
    Sposta il progetto di una posizione nell'ordine manuale.
    direzione: 'up' o 'down'
    """
    if direzione not in ("up", "down"):
        return False

    riallinea_ordine_progetti()
    conn = connetti()
    c = conn.cursor()

    try:
        c.execute(
            """
            SELECT ordine_manuale
            FROM progetti
            WHERE id_progetto = ?
              AND attivo = 1
              AND (archiviato = 0 OR archiviato IS NULL)
            """,
            (id_progetto,),
        )
        row = c.fetchone()
        if not row:
            conn.close()
            return False

        ordine_corrente = int(row[0] or 0)
        ordine_target = ordine_corrente - 1 if direzione == "up" else ordine_corrente + 1
        if ordine_target < 1:
            conn.close()
            return False

        c.execute(
            """
            SELECT id_progetto
            FROM progetti
            WHERE attivo = 1
              AND (archiviato = 0 OR archiviato IS NULL)
              AND ordine_manuale = ?
            """,
            (ordine_target,),
        )
        target_row = c.fetchone()
        if not target_row:
            conn.close()
            return False

        id_target = target_row[0]
        c.execute(
            "UPDATE progetti SET ordine_manuale = ? WHERE id_progetto = ?",
            (ordine_target, id_progetto),
        )
        c.execute(
            "UPDATE progetti SET ordine_manuale = ? WHERE id_progetto = ?",
            (ordine_corrente, id_target),
        )
        conn.commit()
        conn.close()
        return True
    except Exception as e:
        print(f"Errore spostamento progetto: {e}")
        conn.rollback()
        conn.close()
        return False

