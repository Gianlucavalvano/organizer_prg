def verifica_e_aggiorna_colonna(tabella, colonna, tipo_dati):
    """Controlla se una colonna c'Ã¨. Se manca, la aggiunge senza cancellare i dati."""
    conn = connetti()
    c = conn.cursor()
    try:
        c.execute(
            """
            SELECT 1
            FROM information_schema.columns
            WHERE table_schema = 'public'
              AND table_name = %s
              AND column_name = %s
            """,
            (tabella, colonna),
        )
        if c.fetchone() is None:
            c.execute(f"ALTER TABLE {tabella} ADD COLUMN {colonna} {tipo_dati}")
            conn.commit()
    except Exception:
        pass # Se fallisce (es. tabella non esiste ancora) va avanti
    finally:
        conn.close()

