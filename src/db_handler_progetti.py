def hash_password(password: str) -> str:
    """
    Hash PBKDF2-SHA256 in formato:
    pbkdf2_sha256$<iter>$<salt_hex>$<hash_hex>
    """
    raw = (password or "").encode("utf-8")
    iterations = 200000
    salt = secrets.token_bytes(16)
    digest = hashlib.pbkdf2_hmac("sha256", raw, salt, iterations)
    return f"pbkdf2_sha256${iterations}${salt.hex()}${digest.hex()}"


def verifica_password(password: str, stored_hash: str) -> bool:
    """
    Verifica hash PBKDF2.
    CompatibilitÃ  legacy: se hash non in formato noto, confronto diretto.
    """
    if not stored_hash:
        return False
    if stored_hash.startswith("pbkdf2_sha256$"):
        try:
            _, it_s, salt_hex, digest_hex = stored_hash.split("$", 3)
            iterations = int(it_s)
            salt = bytes.fromhex(salt_hex)
            expected = bytes.fromhex(digest_hex)
            current = hashlib.pbkdf2_hmac(
                "sha256", (password or "").encode("utf-8"), salt, iterations
            )
            return hmac.compare_digest(current, expected)
        except Exception:
            return False
    return (password or "") == stored_hash


