def _adapt_params(query: str) -> str:
    # Consente di mantenere i placeholder "?" legacy anche con psycopg (%s).
    return query.replace("?", "%s")


class CompatCursor:
    def __init__(self, cursor):
        self._cursor = cursor

    def execute(self, query, params=None):
        if params is None:
            return self._cursor.execute(_adapt_params(query))
        return self._cursor.execute(_adapt_params(query), params)

    def executemany(self, query, params_seq):
        return self._cursor.executemany(_adapt_params(query), params_seq)

    def fetchone(self):
        return self._cursor.fetchone()

    def fetchall(self):
        return self._cursor.fetchall()

    def __iter__(self):
        return iter(self._cursor)

    def __getattr__(self, name):
        return getattr(self._cursor, name)


class CompatConnection:
    def __init__(self, conn):
        self._conn = conn

    def cursor(self):
        return CompatCursor(self._conn.cursor())

    def commit(self):
        return self._conn.commit()

    def rollback(self):
        return self._conn.rollback()

    def close(self):
        return self._conn.close()

    def __getattr__(self, name):
        return getattr(self._conn, name)


def connetti():
    return CompatConnection(psycopg.connect(get_postgres_dsn()))


