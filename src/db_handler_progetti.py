def owner_filter_sql(alias: str = "", column: str = "owner_user_id", leading_and: bool = True):
    """
    Ritorna clausola SQL + params per filtrare per utente corrente.
    - ADMIN o utente non impostato: nessun filtro.
    - Utente normale: filtro su <alias>.<column> = ?
    """
    uid = _current_user_id()
    if uid is None or _is_admin():
        return "", ()
    prefix = f"{alias}." if alias else ""
    head = " AND " if leading_and else ""
    return f"{head}{prefix}{column} = ?", (uid,)
