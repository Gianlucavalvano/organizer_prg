def get_allegato_abs_path(id_allegato):
    inizializza_db()
    conn = connetti()
    c = conn.cursor()
    uid = _current_user_id()
    owner_filter = " AND owner_user_id = ?" if (uid is not None and not _is_admin()) else ""
    c.execute(
        f"SELECT percorso_relativo, nome_file_storage FROM task_allegati WHERE id_allegato = ?{owner_filter}",
        (id_allegato,) if not owner_filter else (id_allegato, uid),
    )
    row = c.fetchone()
    conn.close()

    if not row:
        return None

    rel = row[0] or ""
    nome_storage = row[1] or ""

    # 1) Path assoluto giÃ  salvato nel DB.
    if os.path.isabs(rel) and os.path.exists(rel):
        return rel

    # 2) Prova path relativo su root correnti/legacy.
    candidate_roots = [
        get_project_root(),
        os.path.join(get_project_root(), "src"),
        os.path.join(os.getenv("APPDATA", ""), "Flet", "prg-flet", "flet"),
        os.path.join(os.getenv("APPDATA", ""), "Flet", "prg-flet", "flet", "app"),
    ]

    if rel:
        for root in candidate_roots:
            if not root:
                continue
            candidate = os.path.normpath(os.path.join(root, rel))
            if os.path.exists(candidate):
                return candidate

    # 3) Fallback: cerca il file storage in cartelle attachments note.
    if nome_storage:
        search_roots = [
            get_attachments_dir(),
            os.path.join(os.getenv("APPDATA", ""), "Flet", "prg-flet", "flet", "attachments"),
            os.path.join(os.getenv("APPDATA", ""), "Flet", "prg-flet", "flet", "app", "attachments"),
        ]
        for root in search_roots:
            if not root or not os.path.isdir(root):
                continue
            for dirpath, _, filenames in os.walk(root):
                if nome_storage in filenames:
                    return os.path.join(dirpath, nome_storage)

    return None


