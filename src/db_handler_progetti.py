def leggi_attivita_scadute():
    """
    Elenca Attività Scadute:
    - Progetti con data1_checkpoint < data odierna.
    - Task con data_fine < data odierna e non completati.
    Output tuple:
    (tipo, id_progetto, id_task, descrizione, data_scadenza)
    """
    inizializza_db()
    oggi = datetime.now().strftime("%Y-%m-%d")
    conn = connetti()
    c = conn.cursor()
    owner_filter_p, owner_params_p = owner_filter_sql("p")
    owner_filter_t, owner_params_t = owner_filter_sql("t")

    sql = f"""
        SELECT
            'PROGETTO' AS tipo,
            p.id_progetto AS id_progetto,
            NULL AS id_task,
            p.nome_progetto AS descrizione,
            SUBSTR(COALESCE(p.data1_checkpoint, ''), 1, 10) AS data_scadenza
        FROM progetti p
        WHERE p.attivo = 1
          AND (p.archiviato = 0 OR p.archiviato IS NULL)
          {owner_filter_p}
          AND TRIM(COALESCE(p.data1_checkpoint, '')) <> ''
          AND to_date(SUBSTR(p.data1_checkpoint, 1, 10), 'YYYY-MM-DD') < %s::date

        UNION ALL

        SELECT
            'TASK' AS tipo,
            t.id_progetto AS id_progetto,
            t.id_task AS id_task,
            t.titolo AS descrizione,
            SUBSTR(COALESCE(t.data_fine, ''), 1, 10) AS data_scadenza
        FROM task t
        LEFT JOIN progetti p ON p.id_progetto = t.id_progetto
        WHERE t.attivo = 1
          {owner_filter_t}
          AND COALESCE(t.completato, 0) = 0
          AND TRIM(COALESCE(t.data_fine, '')) <> ''
          AND to_date(SUBSTR(t.data_fine, 1, 10), 'YYYY-MM-DD') < %s::date
          AND (p.id_progetto IS NULL OR (p.attivo = 1 AND (p.archiviato = 0 OR p.archiviato IS NULL)))

        ORDER BY data_scadenza ASC, tipo DESC
    """
    params = []
    if owner_filter_p:
        params.extend(owner_params_p)
    params.append(oggi)
    if owner_filter_t:
        params.extend(owner_params_t)
    params.append(oggi)
    c.execute(sql, tuple(params))
    rows = c.fetchall()
    conn.close()
    return rows


